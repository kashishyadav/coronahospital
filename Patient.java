    public void patientShow()
    {
        ArrayList<PatientDTO> al;
        try {
            al = PatientDao.patientList();
//            for(PatientDTO k:al)
//         {
//             System.out.println(k);
//         }
         if(al.isEmpty()==true)
         {
           JOptionPane.showMessageDialog(null, "No patient...!","Error!",JOptionPane.INFORMATION_MESSAGE);
            return;
          }
            DefaultTableModel df=(DefaultTableModel)patienttable.getModel();//table model is return by get model
            df.setRowCount(0);//it will delete all the previous row of the table and insert all the new record from database
            Object obj[]=new Object[4];
            for(PatientDTO p:al)
            {
               obj[0]=p.getPatient_no();
               obj[1]=p.getName();
               obj[2]=p.getPhone();
               obj[3]=p.getAddress();
               df.addRow(obj);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
