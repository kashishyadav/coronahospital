    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        PatientDTO p=new PatientDTO();
        patientname=txtname.getText();
        phone=txtphone.getText();
        address=txtaddress.getText();
        p.setAddress(address);
        p.setName(patientname);
        p.setPhone(phone);
        p.setPatient_no(patientId);
        try {
            boolean status=PatientDao.addPatient(p);
            if(status)
            {
                JOptionPane.showMessageDialog(null,"Record inserted...","Congrats!!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
                autoid();
                patientShow();
            }
        } 
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Exception in  db while adding record"+ex,"Error!!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addbtnActionPerformed

