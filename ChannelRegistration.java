    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
      
        try {
          ChannelDTO c= new ChannelDTO();
          String chid=lblchannelno.getText();
          doctorno=DoctorDao.getidbyName(doctorname.getSelectedItem().toString());
          patientno=PatientDao.getidbyName(jComboBox1.getSelectedItem().toString());
          room=roomno.getValue().toString();
          SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
          date=sdf.format(datechoose.getDate());
          c.setChannel_no(chid);
          c.setDate(date);
          c.setDoctorno(doctorno);
          c.setPatientno(patientno);
          c.setRoomno(room);
          System.out.println("channel obj : "+c);
           // System.out.println("object passed from update function: "+p);
            boolean status=ChannelDao.updateChannel(c);
            System.out.println("worked status..."+status);
            if(status)
            {
                JOptionPane.showMessageDialog(null,"Record UPDATE...","Congrats!!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
                autoid();
                channelShow();
                addbtn.setEnabled(true);
            }
        }
        catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Exception in  db while UDATING record\n"+ex,"Error!!",JOptionPane.ERROR_MESSAGE);
            
            Logger.getLogger(ChannelRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_updatebtnActionPerformed

