    public void doctorList()
    {
       
        try {
            ArrayList<DoctorDTO> al;
            al = DoctorDao.doctorShowList();
            if(al.isEmpty())
       {
           JOptionPane.showMessageDialog(null,"there is no doctor to show in list....");
       }
       else{
        doctorname.removeAll();
        for(DoctorDTO d:al)
        {
            doctorname.addItem(d.getName());
        }
        }
        }catch (SQLException ex) {
            Logger.getLogger(ChannelRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
     }
     public void patientList() 
    {
        try {
            ArrayList<PatientDTO> al=PatientDao.patientShowList();
            if(al.isEmpty())
            {
              JOptionPane.showMessageDialog(null,"there is no patient to show in list....");
             }
            else{
               jComboBox1.removeAll();
               for(PatientDTO p:al)
                {
                   jComboBox1.addItem(p.getName());
                  }
             }
                }
        catch (SQLException ex)
           {
              Logger.getLogger(ChannelRegistration.class.getName()).log(Level.SEVERE, null, ex);
             }
       
        
        
    }

